# This model only concatenates control info right before the feed forward layers, as well as the final ff layer
# Seems to perform worse when concatenating to the final linear layer
class TransformerModel_with_Category_edited(nn.Module):

    def __init__(self, ntoken: int, d_model: int, nhead: int, d_hid: int,
                 nlayers: int, dropout: float = 0.5):
        super().__init__()
        self.model_type = 'Transformer'
        self.pos_encoder = PositionalEncoding(d_model, dropout)
        encoder_layers = TransformerEncoderLayer_edited(d_model, nhead, d_hid, dropout, batch_first=True)
        self.transformer_encoder = TransformerEncoder_edited(encoder_layers, nlayers)
        self.encoder = nn.Embedding(ntoken, d_model)
        self.category_embedding = nn.Embedding(6, d_model)
        self.d_model = d_model
        self.decoder = nn.Linear(d_model, ntoken)

        self.init_weights()

    def init_weights(self) -> None:
        initrange = 0.1
        self.encoder.weight.data.uniform_(-initrange, initrange)
        self.decoder.bias.data.zero_()
        self.decoder.weight.data.uniform_(-initrange, initrange)

    def forward(self, src: Tensor, category: Tensor, src_mask: Tensor = None) -> Tensor:
        """
        Args:
            src: Tensor, shape [seq_len, batch_size]
            src_mask: Tensor, shape [seq_len, seq_len]

        Returns:
            output Tensor of shape [seq_len, batch_size, ntoken]
        """
        src = self.encoder(src) * math.sqrt(self.d_model)
        cat = self.category_embedding(category)
        src = self.pos_encoder(src)
        output = self.transformer_encoder(src, cat, src_mask)
        print('d_model: ', self.d_model)
        print('output size: ', output.size())
        output = self.decoder(output) # This is what's different in this model.
        return output
    
